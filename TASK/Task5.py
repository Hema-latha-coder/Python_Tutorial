"""
types of datatypes
1.Number data type
    int,float
2.Sequence data type
    string,list,tuple
3.Set data type
4.dictionary data type
5.boolean data type
6.None data type
"""
#number data type
a=5
b=10.1
print(type(a),a)
print(type(b),b)

#string data type
c="hema"
print(type(c),c)

#list 
lt=["chennai","Coimbatore","Madurai"]
print(type(lt),lt)

#tuple
t1=(10,20,30,40)
print(type(t1),t1)

#set
s1={"Python","AI","ML","JS","DS"}
print(type(s1),s1)

#dictionary
dict1={1:"Python",2:"AI",3:"ML",4:"JS",5:"DS"}
print(type(dict1),dict1)

#none
a= None
print(type(a),a)

#boolean
a=0
b=1
print(bool(type(a)),a)
print(bool(type(b)),b)