#Types of parameters
'''
1.positional parameter
2.Keyword parameter
3.Default Parameter
4.Variable length Parameter
5.Keyword length parameter
6.Variable length keyword parameter
7.Positional only parameter
8.Keyword only parameter
'''
#positional parameter
def add(a,b):
    print("Add=", a+b)
add(10,2)
#keyword parameter
def sub(a,b):
    print("sub=", a-b)
sub(a=10,b=2)
